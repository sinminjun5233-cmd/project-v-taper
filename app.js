const $ = (s, root=document)=>root.querySelector(s);
const $$ = (s, root=document)=>[...root.querySelectorAll(s)];
const todayISO = () => new Date().toISOString().slice(0,10);
const store = {
  get(k, fallback){ try{return JSON.parse(localStorage.getItem(k)) ?? fallback}catch{return fallback} },
  set(k,v){ localStorage.setItem(k,JSON.stringify(v)) }
};

let selectedDay = Number(store.get("vt.selectedDay", new Date().getDay()));
let timerId = null, remain = 0;

function dayKey(){ return `vt.day.${todayISO()}.${selectedDay}` }
function profile(){ return store.get("vt.profile", {}) }
function saveProfilePatch(patch){ store.set("vt.profile", {...profile(), ...patch}) }

function setupGreeting(){
  const h = new Date().getHours();
  const word = h < 12 ? "Good Morning" : h < 18 ? "Good Afternoon" : "Good Evening";
  $("#greeting").textContent = `${word}, 민준`;
}

function setupTabs(){
  $$(".tab").forEach(btn=>{
    btn.onclick=()=>{
      $$(".tab").forEach(b=>b.classList.remove("active"));
      btn.classList.add("active");
      $$(".screen").forEach(s=>s.classList.remove("active"));
      $("#" + btn.dataset.tab).classList.add("active");
    }
  });
  $$("[data-go]").forEach(btn=>btn.onclick=()=> $(`.tab[data-tab="${btn.dataset.go}"]`).click());
}

function setupDaySelect(){
  const names = ["일","월","화","수","목","금","토"];
  $("#daySelect").innerHTML = "";
  for(let i=0;i<7;i++){
    const opt=document.createElement("option");
    opt.value=i; opt.textContent=`${names[i]} · ${routine[i].ko}`;
    $("#daySelect").appendChild(opt);
  }
  $("#daySelect").value = selectedDay;
  $("#daySelect").onchange=e=>{
    selectedDay=Number(e.target.value);
    store.set("vt.selectedDay", selectedDay);
    renderAll();
  };
}

function setupInputs(){
  const p = profile();
  $("#weightToday").value = p.weight ?? "";
  $("#proteinToday").value = p.protein ?? "";
  $("#waterToday").value = p.water ?? "";
  $("#sleepToday").value = p.sleep ?? "";
  ["Creatine","Protein","Steps","Skin"].forEach(n=>{
    $("#h"+n).checked = !!p["h"+n];
  });
  ["weightToday","proteinToday","waterToday","sleepToday"].forEach(id=>{
    $("#"+id).addEventListener("input",()=>{
      saveProfilePatch({
        weight:$("#weightToday").value,
        protein:$("#proteinToday").value,
        water:$("#waterToday").value,
        sleep:$("#sleepToday").value
      });
      updateMetricBars();
    });
  });
  $$(".habit").forEach(h=>h.addEventListener("change",()=>saveProfilePatch({
    hCreatine:$("#hCreatine").checked,
    hProtein:$("#hProtein").checked,
    hSteps:$("#hSteps").checked,
    hSkin:$("#hSkin").checked
  })));
  $("#resetDay").onclick=()=>{
    store.set(dayKey(), {});
    renderAll();
  };
  $("#saveWeight").onclick=saveWeight;
}

function updateMetricBars(){
  const protein = Math.min(100, (Number($("#proteinToday").value)||0)/150*100);
  const water = Math.min(100, (Number($("#waterToday").value)||0)/3000*100);
  $("#proteinBar").style.width = protein + "%";
  $("#waterBar").style.width = water + "%";
  const w = Number($("#weightToday").value);
  $("#weightDelta").textContent = w ? `목표까지 ${(w-73).toFixed(1)}kg` : "목표 73kg";
}

function renderAll(){
  const r = routine[selectedDay];
  $("#todayName").textContent = r.ko;
  $("#todayCardio").textContent = r.cardio;
  renderWorkout();
  renderLogs();
  updateProgress();
  renderChart();
  updateMetricBars();
}

function renderWorkout(){
  const r = routine[selectedDay];
  const saved = store.get(dayKey(), {});
  const wrap = $("#workoutCards");
  wrap.innerHTML = "";
  r.items.forEach((it,i)=>{
    const id = `ex${i}`;
    const card = document.createElement("article");
    card.className = "exercise-card" + (saved[id] ? " done":"");
    card.innerHTML = `
      <div class="exercise-main">
        <div class="exercise-icon">${it[5]}</div>
        <div class="exercise-title">
          <h3>${it[0]}</h3>
          <div class="chips">
            <span class="chip">${it[1]}세트</span>
            <span class="chip">${it[2]}</span>
            <span class="chip">휴식 ${it[3]}</span>
          </div>
        </div>
        <input type="checkbox" ${saved[id]?"checked":""}>
      </div>
      <details>
        <summary>자세 / 주의사항</summary>
        <p>${it[4]}</p>
      </details>
      <div class="quick-timer">
        ${it[3] !== "-" ? `<button data-sec="${parseRest(it[3])}">휴식 ${it[3]}</button>` : ""}
        <button data-log="${it[0]}">기록하기</button>
      </div>
    `;
    const cb = $("input[type=checkbox]", card);
    cb.onchange=()=>{
      const now = store.get(dayKey(), {});
      now[id] = cb.checked;
      store.set(dayKey(), now);
      card.classList.toggle("done", cb.checked);
      updateProgress();
    };
    const tBtn = $("[data-sec]", card);
    if(tBtn) tBtn.onclick=()=>startTimer(Number(tBtn.dataset.sec));
    $("[data-log]", card).onclick=()=> {
      $(".tab[data-tab='log']").click();
      setTimeout(()=>document.querySelector(`[data-log-name="${CSS.escape(it[0])}"]`)?.scrollIntoView({behavior:"smooth",block:"center"}),50);
    };
    wrap.appendChild(card);
  });
}

function parseRest(rest){
  const m = rest.match(/(\d+)/);
  return m ? Number(m[1]) : 60;
}

function updateProgress(){
  const total = routine[selectedDay].items.length;
  const saved = store.get(dayKey(), {});
  const done = Object.values(saved).filter(Boolean).length;
  const pct = total ? Math.round(done/total*100) : 0;
  $("#homePct").textContent = pct + "%";
  $("#dialFg").style.strokeDashoffset = 364.4 * (1 - pct/100);
  $("#missionText").textContent = pct === 100 ? "MISSION COMPLETE" : "Mission not complete yet";
}

function renderLogs(){
  const wrap = $("#logCards"); wrap.innerHTML = "";
  routine[selectedDay].items.forEach(it=>{
    if(it[3] === "-") return;
    const name = it[0];
    const data = store.get(`vt.log.${name}`, {});
    const card = document.createElement("article");
    card.className = "log-card glass";
    card.dataset.logName = name;
    card.innerHTML = `
      <h3>${name}</h3>
      <p class="rec">${recommend(data)}</p>
      <div class="set-grid">
        <input class="kg" type="number" step="0.5" placeholder="kg" value="${data.kg ?? ""}">
        <input class="r1" type="number" placeholder="1세트" value="${data.r1 ?? ""}">
        <input class="r2" type="number" placeholder="2세트" value="${data.r2 ?? ""}">
        <input class="r3" type="number" placeholder="3세트" value="${data.r3 ?? ""}">
      </div>
      <button class="log-save">Save Record</button>
    `;
    $(".log-save",card).onclick=()=>{
      const next = {
        kg:$(".kg",card).value,
        r1:$(".r1",card).value,
        r2:$(".r2",card).value,
        r3:$(".r3",card).value,
        date:todayISO()
      };
      store.set(`vt.log.${name}`, next);
      $(".rec",card).textContent = recommend(next);
      navigator.vibrate?.(80);
    };
    wrap.appendChild(card);
  });
}

function recommend(d){
  const kg = Number(d.kg);
  const reps = [Number(d.r1), Number(d.r2), Number(d.r3)];
  if(!kg || reps.some(x=>!x)) return "기록하면 다음 무게를 추천해줄게.";
  if(reps.every(x=>x>=12)) return `다음 운동: ${kg+2.5}kg 도전 추천.`;
  if(reps.every(x=>x>=10)) return "다음 운동: 같은 무게로 12회까지 채우기.";
  if(reps.some(x=>x<8)) return `무게가 약간 무거움. ${Math.max(kg-2.5,0)}kg도 가능.`;
  return "현재 무게 유지. 마지막 1~2개가 힘든 정도면 적절함.";
}

function saveWeight(){
  const w = Number($("#weightToday").value);
  if(!w) return;
  const arr = store.get("vt.weights", []);
  const filtered = arr.filter(x=>x.date !== todayISO());
  filtered.push({date:todayISO(), weight:w});
  store.set("vt.weights", filtered.slice(-14));
  renderChart();
  navigator.vibrate?.(80);
}

function renderChart(){
  const chart = $("#weightChart");
  const arr = store.get("vt.weights", []);
  if(arr.length === 0){
    chart.innerHTML = `<p class="soft">체중을 입력하고 Save를 누르면 그래프가 생겨.</p>`;
    return;
  }
  const vals = arr.map(x=>x.weight);
  const min = Math.min(...vals)-.5, max = Math.max(...vals)+.5;
  chart.innerHTML = "";
  arr.forEach(x=>{
    const h = 20 + ((x.weight-min)/(max-min))*65;
    const bar = document.createElement("div");
    bar.className = "bar";
    bar.style.height = h + "px";
    bar.innerHTML = `<span>${x.weight}</span>`;
    chart.appendChild(bar);
  });
}

function startTimer(sec){
  clearInterval(timerId);
  remain = sec;
  $("#timerPill").classList.add("show");
  tick();
  timerId = setInterval(()=>{
    remain--;
    tick();
    if(remain <= 0){
      clearInterval(timerId);
      navigator.vibrate?.([250,120,250]);
    }
  },1000);
}
function tick(){
  const m = String(Math.floor(remain/60)).padStart(2,"0");
  const s = String(remain%60).padStart(2,"0");
  $("#timerDisplay").textContent = `${m}:${s}`;
}
$("#timerStop").onclick=()=>{
  clearInterval(timerId);
  remain = 0;
  tick();
  $("#timerPill").classList.remove("show");
};

